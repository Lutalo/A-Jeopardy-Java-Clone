
public class AnswerCard
{
    private String content;
    private int amount;
    
    public AnswerCard(String content, int amount)
    {
	this.content = content;
	this.amount = amount;
    }
    public String getContent()
    {
        return content;
    }
    public void setContent(String content)
    {
        this.content = content;
    }
    
    
}
